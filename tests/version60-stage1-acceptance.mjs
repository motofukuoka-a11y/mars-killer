import assert from 'node:assert/strict';
import Version6Platform from '../services/v6/Version6Platform.js';
const fareEngine = {calculateUsedSectionFare(request) { return {amount_yen: request.used_section_fare, discount_re_evaluated: false}; }};
const p = new Version6Platform({debug: true, fareEngine});
let r = p.execute({business_mode:'passenger_refund',ticket_type:'ordinary',travel_state:'before_travel',face_value:5280,refund_fee:220});
assert.equal(r.result.refund_amount,5060);
r = p.execute({business_mode:'passenger_refund',ticket_type:'ordinary',travel_state:'after_travel',face_value:5280,used_section_fare:1800,unused_business_km:100,refund_fee:220});
assert.equal(r.result.status,'not_eligible');
r = p.execute({business_mode:'passenger_refund',ticket_type:'ordinary',travel_state:'after_travel',face_value:5280,used_section_fare:1800,unused_business_km:101,refund_fee:220});
assert.equal(r.result.refund_amount,3260);
r = p.execute({business_mode:'accident_handling',ticket_type:'limited_express',travel_state:'after_travel',incident_type:'delay',delay_minutes:119,express_charge:2640});
assert.equal(r.result.refund_amount,0);
r = p.execute({business_mode:'accident_handling',ticket_type:'limited_express',travel_state:'after_travel',incident_type:'delay',delay_minutes:120,express_charge:2640});
assert.equal(r.result.refund_amount,2640);
r = p.execute({business_mode:'accident_handling',ticket_type:'ordinary',travel_state:'after_travel',incident_type:'alternate_route',alternative_route:'代替経路',original_fare:5000,actual_fare:5500,original_charge:2000,actual_charge:1500,original_facility:1000,actual_facility:1000});
assert.equal(r.result.additional_collection,0); assert.equal(r.result.refund_amount,500);
r = p.execute({business_mode:'accident_handling',ticket_type:'ordinary',travel_state:'after_travel',incident_type:'travel_cancelled'});
assert.equal(r.result.status,'multiple_choices_available');
assert.ok(p.audit.entries.length >= 5);
